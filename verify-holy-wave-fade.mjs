import { chromium } from 'file:///C:/Users/san%20day/.codex/node_modules/playwright/index.mjs';
import fs from 'fs/promises';
import path from 'path';
const outDir = 'D:/tryings/vibecoding/emoji-survivors/output/web-game/verify-holy-wave-fade';
await fs.mkdir(outDir, { recursive: true });
const browser = await chromium.launch({ headless: true });
const page = await browser.newPage({ viewport: { width: 1600, height: 960 } });
await page.goto('file:///D:/tryings/vibecoding/emoji-survivors/index.html');
await page.evaluate(() => { window.debug_game.unlockAllClasses(); window.debug_game.selectClass('fire'); window.debug_game.startRun(); window.debug_game.setSpawningEnabled(false); window.debug_game.grantXp(1000); });
await page.waitForTimeout(250);
await page.keyboard.press('Digit1');
await page.waitForTimeout(180);
const during = JSON.parse(await page.evaluate(() => window.render_game_to_text()));
await page.screenshot({ path: path.join(outDir, 'holy-wave-during.png') });
await page.waitForTimeout(520);
const later = JSON.parse(await page.evaluate(() => window.render_game_to_text()));
await page.screenshot({ path: path.join(outDir, 'holy-wave-later.png') });
await fs.writeFile(path.join(outDir, 'checks.json'), JSON.stringify({ duringEffects: during.activeEffects, laterEffects: later.activeEffects, duringMode: during.mode, laterMode: later.mode }, null, 2));
await browser.close();
