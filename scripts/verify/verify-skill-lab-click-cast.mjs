import { chromium } from 'file:///C:/Users/san%20day/.codex/node_modules/playwright/index.mjs';
import fs from 'fs/promises';
import path from 'path';
const outDir = 'D:/tryings/vibecoding/emoji-survivors/output/web-game/verify-skill-lab-click-cast';
await fs.mkdir(outDir, { recursive: true });
const browser = await chromium.launch({ headless: true });
const page = await browser.newPage({ viewport: { width: 1600, height: 960 } });
await page.goto('file:///D:/tryings/vibecoding/emoji-survivors/skill-lab.html');
await page.waitForTimeout(1200);
await page.evaluate(() => window.advanceTime(7000));
await page.waitForTimeout(100);
await page.click('[data-skill-slot="1"]');
await page.waitForTimeout(220);
const state = JSON.parse(await page.evaluate(() => window.render_game_to_text()));
await page.screenshot({ path: path.join(outDir, 'click-cast-slot1.png') });
await fs.writeFile(path.join(outDir, 'state.json'), JSON.stringify(state, null, 2));
await browser.close();
